# len function
t=1,2,55,12.5,55,'vivek','kumar'
print(len(t))  #  7

# count function returns the no of occurrence of a given element.

print(t.count(55))  # 2

# index() : it gives the first occurence of the given element.

print(t.index(55))  # 2

# sorted()
p=1,44,3.4,55,22.4,55
print(sorted(p,reverse=True))

print(max(p))
print(min(p))




